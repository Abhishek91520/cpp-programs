/*
C++ Program to Add Two Numbers
*/

#include <iostream>

using namespace std;

int main()
{
    float number1 = 0, number2 = 0, result = 0;
    cout<<"Enter first number = ";
    cin>>number1;
    cout<<"Enter second number = ";
    cin>>number2;
    result = number1 + number2;
    cout<<"Result = "<<result<<endl;
    return 0;
}
