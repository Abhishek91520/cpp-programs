/*
C++ Program to Find Size of int, float, double and char in Your System
*/

#include <iostream>

using namespace std;

int main()
{
    cout << "int = " <<sizeof(int)<<endl;
    cout << "Float = " <<sizeof(float)<<endl;
    cout << "Char = " <<sizeof(char)<<endl;
    cout << "Double = " <<sizeof(double)<<endl;
    return 0;
}
